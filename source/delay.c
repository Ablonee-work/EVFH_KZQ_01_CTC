//函数名:delay_10ms
//功  能:以10ms为基数进行延时
//参  数:
/************************************************************************/

void delay_10ms(unsigned int Count)
{
	int Temp = 0;      
	         									//定义循环变量
	for(;Count>0; Count--)
	{
		for(Temp=835; Temp>0; Temp--)  		 	//循环次数控制
    	{
	    	NOP();           			    	//消耗一个指令周期的时间
    	}	
	}
}

