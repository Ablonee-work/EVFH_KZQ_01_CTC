//函数名:delay_10us
//功  能:以10us为基数进行延时
//参  数:
/************************************************************************/
void delay_10us(unsigned int Count)
{
		for(; Count>0; Count--)   				//循环次数控制
    	{
	    	NOP();             				   //消耗一个指令周期的时间
    	}	
}
